/// Embedded star catalog data (Hipparcos J2000).
use crate::fixstar::LeStar;
use std::sync::LazyLock;

fn s(name: &str) -> [i8; 32] {
    let mut out = [0i8; 32];
    for (i, &b) in name.as_bytes().iter().enumerate().take(31) {
        out[i] = b as i8;
    }
    out
}

macro_rules! star {
    ($name:expr, $ra:expr, $dec:expr, $pm_ra:expr, $pm_dec:expr, $par:expr, $mag:expr) => {
        LeStar {
            name: s($name),
            ra: $ra,
            dec: $dec,
            parallax: $par,
            pm_ra: $pm_ra,
            pm_dec: $pm_dec,
            mag: $mag,
        }
    };
}

