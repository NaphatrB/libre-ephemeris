/// Planetary fundamental arguments (mean longitudes of planets) at time t (Julian centuries).
pub struct PlanetArgs {
    pub l_me: f64, pub l_ve: f64, pub l_e: f64, pub l_ma: f64,
    pub l_j: f64, pub l_sa: f64, pub l_u: f64, pub l_ne: f64,
    pub p_a: f64,
}

/// Compute planetary fundamental arguments from IERS Conventions 2010 eq 5.6.
pub fn planetary_args(t: f64) -> PlanetArgs {
    let as2r = std::f64::consts::PI / (180.0 * 3600.0);
    let t2 = t * t; let t3 = t2 * t; let t4 = t3 * t;
    PlanetArgs {
        l_me: (908103.259872 + 538101628.688982 * t + 0.21555 * t2 - 0.001270 * t3 - 0.000255 * t4) % 1296000.0 * as2r,
        l_ve: (655127.852406 + 210664136.433548 * t + 0.49895 * t2 + 0.004797 * t3 + 0.000569 * t4) % 1296000.0 * as2r,
        l_e: (361679.348438 + 129597742.282742 * t - 0.10933 * t2 + 0.000250 * t3 - 0.000139 * t4) % 1296000.0 * as2r,
        l_ma: (1279557.798774 + 68905077.493988 * t + 0.85609 * t2 + 0.001666 * t3 + 0.000112 * t4) % 1296000.0 * as2r,
        l_j: ((120.0 * 3600.0 * 5.0 + 123.0 * 3600.0 + 30.0 * 60.0 + 59.0 + 0.47)
            + (10925778.748354 * t - 1.43909 * t2 - 0.000155 * t3 + 0.000185 * t4)) % 1296000.0 * as2r,
        l_sa: ((120.0 * 3600.0 * 9.0 + 60.0 * 3600.0 + 10.0 * 60.0 + 56.0 + 3.31)
            + (4399609.659336 * t + 1.33437 * t2 + 0.000520 * t3 - 0.000098 * t4)) % 1296000.0 * as2r,
        l_u: ((120.0 * 3600.0 * 18.0 + 20.0 * 3600.0 + 34.0 * 60.0 + 35.0 + 38.63)
            + (1545245.002824 * t + 0.61762 * t2 + 0.000256 * t3 + 0.000016 * t4)) % 1296000.0 * as2r,
        l_ne: ((120.0 * 3600.0 * 25.0 + 250.0 * 3600.0 + 2.0 * 60.0 + 57.0 + 36.67)
            + (786547.897004 * t - 0.16691 * t2 + 0.000135 * t3 + 0.000024 * t4)) % 1296000.0 * as2r,
        p_a: ((120.0 * 3600.0 * 2.0 + 100.0 * 3600.0 + 2.0 * 60.0 + 9.0 + 31.60)
            + (-260.87944 * t)) % 1296000.0 * as2r,
    }
}

/// Compute IAU 2000A nutation in longitude (dpsi) and obliquity (deps) in radians.
///
/// Uses the full term table if available (generated from IERS data), 
/// otherwise falls back to the 77 dominant lunisolar terms.
pub fn nutation_lon_obl(t: f64) -> (f64, f64) {
    let fa = fundamental_args(t);
    let pa = planetary_args(t);

    let terms = if !NUT_TERMS_FULL.is_empty() { NUT_TERMS_FULL } else { NUT_TERMS_77 };

    let mut dpsi = 0.0;
    let mut deps = 0.0;

    for term in terms {
        let arg = term.0 as f64 * fa.l
            + term.1 as f64 * fa.lp
            + term.2 as f64 * fa.f
            + term.3 as f64 * fa.d
            + term.4 as f64 * fa.om
            + term.5 as f64 * pa.l_me
            + term.6 as f64 * pa.l_ve
            + term.7 as f64 * pa.l_e
            + term.8 as f64 * pa.l_ma
            + term.9 as f64 * pa.l_j
            + term.10 as f64 * pa.l_sa
            + term.11 as f64 * pa.l_u
            + term.12 as f64 * pa.l_ne
            + term.13 as f64 * pa.p_a;
        let (s, c) = arg.sin_cos();
        // Use indices 14-17 for the main coefficients, 18-21 for time-dependent
        dpsi += term.14 * s + term.15 * c + term.18 * s * t + term.19 * c * t;
        deps += term.16 * c + term.17 * s + term.20 * c * t + term.21 * s * t;
    }

    let factor = 0.1e-6 * std::f64::consts::PI / (180.0 * 3600.0);
    (dpsi * factor, deps * factor)
}
